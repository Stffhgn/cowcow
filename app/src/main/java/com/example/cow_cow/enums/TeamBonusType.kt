package com.example.cow_cow.enums

enum class TeamBonusType {
    EXTRA_POINTS,      // Grants extra points to the team
    SHARED_POWER_UP,   // Activates a power-up for all team members
    TEAM_SPEED_BOOST,  // Boosts the speed of the entire team for a period
    TEAM_HEALING,      // Restores team members' health or removes penalties
    SCORE_MULTIPLIER,  // Doubles or triples the team's score for a limited time
    TEAM_INVULNERABILITY // Makes the entire team immune to penalties for a duration
}